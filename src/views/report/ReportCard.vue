    } catch (error) {
        mergeJobStatus.value = 'Failed';
        toast.add({
            severity: 'error',
            summary: 'Error',
            detail: error.message || 'Failed to start merged PDF generation.',
            life: 5000
        });
    }
};

const pollMergedPdfStatus = () => {
    if (pollingInterval.value) clearInterval(pollingInterval.value);
    pollingInterval.value = setInterval(async () => {
        if (!mergeJobId.value) return;
        try {
            const statusData = await reportService.checkMergedPdfStatus(mergeJobId.value);
            // status: "processing", "completed", "failed", etc.
            if (statusData.status === 'completed') {
                mergeJobStatus.value = 'Ready';
                canDownloadMergedPdf.value = true;
                clearInterval(pollingInterval.value);
            } else if (statusData.status === 'failed') {
                mergeJobStatus.value = 'Failed';
                canDownloadMergedPdf.value = false;
                clearInterval(pollingInterval.value);
            } else {
                mergeJobStatus.value = 'Processing';
                canDownloadMergedPdf.value = false;
            }
        } catch (error) {
            mergeJobStatus.value = 'Failed';
            canDownloadMergedPdf.value = false;
            clearInterval(pollingInterval.value);
            toast.add({
                severity: 'error',
                summary: 'Error',
                detail: error.message || 'Failed to check job status.',
                life: 5000
            });
        }
    }, 3000); // poll every 3 seconds
};

