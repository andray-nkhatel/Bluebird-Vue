            detail: 'Report card email is being processed in the background.',
            life: 4000
        });
    } catch (error) {
        toast.add({
            severity: 'error',
            summary: 'Error',
            detail: error.message || 'Failed to send report card email',
            life: 5000
        });
    } finally {
        sendingClassReportCardsEmail.value = false;
    }
};

