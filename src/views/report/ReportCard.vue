            activeAcademicYear.value = activeYear;
            const activeYearValue = parseInt(activeYear.name);
            academicYear.value = activeYearValue;
            classAcademicYear.value = activeYearValue;
            console.log('🔍 Set Active Year:', activeYearValue);
